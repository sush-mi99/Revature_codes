package frst;

public class RelationalOperators {
	private int x;
	private int y;
	
	public RelationalOperators(int x,int y) {
		this.x=x; 
		this.y=y;
	if(x % y ==2) {
    // == operator
    System.out.println(x == y);  // false

    // != operator
    System.out.println(x != y);  // true

    // > operator
    System.out.println(x > y);  // false

    // < operator
    System.out.println(x < y);  // true

    // >= operator
    System.out.println(x >= y);  // false

    // <= operator
    System.out.println(x <= y);  // true
	}
	else {
		System.out.println("Not Possible");
	}
  }
}
